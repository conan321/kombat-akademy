			<div>09/14/2023</div>
			<p>Hi. I'm excited to announce the launch of the new site! The site has been completely revamped and will be focusing on the newest fighting game, <em>Mortal Kombat 1</em>. I've improved many features from the previous site and plan on adding more over time.</p>
			<p>
				<strong>Move List</strong> - The move list has its own page and has been redesigned. You can select a character and optionally a Kameo, as well as the version date to see their moves and frame data. Hovering over a move will show that move's frame data on the right-hand side, and clicking on a move will lock that move's frame data in-place. There are also 2 different ways to view the move list: Default or Table. Please note that not all data has been entered in yet due to the game's recent release.
			</p>
			<p>
				<strong>Combos</strong> - The combos page has its own page. You can select a character and optionally a Kameo. Combos are separated into 3 categories: Easy, Medium, and Hard. You can also filter combos by Kameo, Difficulty, and Tag. Each combos has a video player button that when clicked will play a clip of the selected combo. A separate video player which is positioned at the top will play all combos as a playlist. Please note that there are little to no combos added yet due to the game's recent release.
			</p>
			<p>
				<strong>Characters</strong> - The characters page has links to each individual character guide. There is also a button to view each character's stats. Character guides are split into different sections with links to see more information for that particular section. There will also be a Gameplay section which will include videos of that character being played by tournament players. Currently there are no stats or guides for characters.
			</p>
			<p>
				<strong>Where's MK11?</strong> - I understand that there are some who still play <em>Mortal Kombat 11</em>, but I've made the decision to temporarily remove that section from the site for a number of reasons. With the recent release of <em>Mortal Kombat 1</em>, I will be mostly focusing on that game. The previous site was poorly configured and was using up too many resources which could interfere with the current site and cause it to go down. Additionally, all of the videos that were used for the previous site were hosted on Gfycat, which recently shut down so it will take time to re-upload them and fix all the broken links. This also gives me the opportunity to re-work the previous site. Again, this is only temporary and I'm unsure how long it will take but if you'd like any information for that game, feel free to contact me. Thank you for understanding.
			</p>
			<p>
				I'm hoping that everyone will find this site to be a useful resource and I look forward to updating it with more information as the game continues to evolve. Thanks for reading!
			</p>
